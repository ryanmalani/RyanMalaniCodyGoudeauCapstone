package com.company.RyanMalaniCodyGoudeauCapstone.model;

import javax.validation.constraints.Digits;
import javax.validation.constraints.NotEmpty;
import javax.validation.constraints.Size;
import java.math.BigDecimal;
import java.util.Objects;

public class Processing_Fee {

    // properties

    @NotEmpty(message = "You must provide a value for product type.")
    @Size(max = 20, message = "You must provide a value less than 20 characters")
    private String productType;
    @Digits(integer = 2,fraction = 2)
    private BigDecimal fee;

    // constructors

    public Processing_Fee() {
    }

    public Processing_Fee(String productType, BigDecimal fee) {
        this.productType = productType;
        this.fee = fee;
    }

    // getters and setters

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public BigDecimal getFee() {
        return fee;
    }

    public void setFee(BigDecimal fee) {
        this.fee = fee;
    }

    // equals and hashCode

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Processing_Fee that = (Processing_Fee) o;
        return Objects.equals(productType, that.productType) && Objects.equals(fee, that.fee);
    }

    @Override
    public int hashCode() {
        return Objects.hash(productType, fee);
    }

    // toString

    @Override
    public String toString() {
        return "Processing_Fee{" +
                "productType='" + productType + '\'' +
                ", fee=" + fee +
                '}';
    }
}
